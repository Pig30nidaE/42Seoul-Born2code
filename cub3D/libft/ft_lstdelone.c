#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	t_list	*temp;

	if (!del || !lst)
		return ;
	temp = lst;
	del(lst->content);
	lst = lst->next;
	free(temp);
}
